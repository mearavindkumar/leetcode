	public int maximumCount(int[] nums) {
//		int lastNegativeNumber = search(nums, 0);
		int lastNegativeNumber = 0;
		int firstPositiveNumber = nums.length - search(nums, 1);

//		System.out.println(lastNegativeNumber + " \n" + firstPositiveNumber);

		return lastNegativeNumber > firstPositiveNumber ? lastNegativeNumber : firstPositiveNumber;
	}

	private int search(int[] nums, int target) {
		int start = 0;
		int end = nums.length;
		while (start < end) {
			int mid = start + (end - start) / 2;
			if (nums[mid] < target) {
				start = mid + 1;
			} else {
				end = mid;
			}
		}
		return start;
	}

